-- イベントメトリクスサンプルデータ（過去1週間分）
INSERT INTO event_metrics (event_id, date, daily_posts, daily_views, daily_reactions, daily_participants, card_conversions)
SELECT
  e.id,
  CURRENT_DATE - i,
  FLOOR(RANDOM() * 5 + 1)::INTEGER,
  FLOOR(RANDOM() * 100 + 20)::INTEGER,
  FLOOR(RANDOM() * 20 + 5)::INTEGER,
  FLOOR(RANDOM() * 10 + 2)::INTEGER,
  FLOOR(RANDOM() * 3 + 1)::INTEGER
FROM events e
CROSS JOIN generate_series(0, 6) AS i
WHERE e.status = 'active';
